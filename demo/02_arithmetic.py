# 整数
print("整数：")
print("2 + 3 =", 2+3)
print("3 - 2 =", 3-2)
print("2 * 3 =", 2*3)
print("3 / 2 =", 3/2)
print("---------------------------------")

# 乘方
print("3 ^ 2 =", 3 ** 2)
print("3 ^ 3 =", 3 ** 3)
print("10 ^ 3 =", 10 ** 3)
print("---------------------------------")

# 括号
print("2 + 3 * 4 =", 2+3*4)
print("(2 + 3) * 4 =", (2+3)*4)



